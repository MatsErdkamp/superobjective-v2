    "    return normalized;",
    "  }",
    "",
    "  __workspaceBucket() {",
    "    return this.env.SO_RLM_WORKSPACE ?? this.env.SO_ARTIFACTS ?? null;",
    "  }",
    "",
    "  __workspaceR2Key(path) {",
    "    const state = this.state ?? {};",
    "    const runId = String(state.runId ?? 'unknown').replace(/[^a-zA-Z0-9._-]+/g, '_');",
    "    return `rlm-workspaces/${runId}${path}`;",
    "  }",
    "",
    "  __globToRegExp(pattern) {",
    "    const normalized = this.__workspacePath(pattern);",
    "    let source = '^';",
    "    for (let index = 0; index < normalized.length; index += 1) {",
    "      const char = normalized[index];",
    "      const next = normalized[index + 1];",
    "      if (char === '*' && next === '*') { source += '.*'; index += 1; continue; }",
    "      if (char === '*') { source += '[^/]*'; continue; }",
    "      if (char === '?') { source += '[^/]'; continue; }",
    "      source += char.replace(/[|\\\\{}()[\\]^$+?.]/g, '\\\\$&');",
    "    }",
    "    return new RegExp(`${source}$`);",
    "  }",
    "",
    "  async __workspaceReadText(path) {",
    "    const normalized = this.__workspacePath(path);",
    "    const rows = this.ctx.storage.sql.exec('SELECT content_text, r2_key FROM session_workspace_entries WHERE path = ? AND entry_type = ? LIMIT 1', normalized, 'file').toArray();",
