    "    matches.push({",
    "      path,",
    "      startChar,",
    "      endChar,",
    "      match: text.slice(startChar, endChar),",
    "      snippet: text.slice(snippetStart, snippetEnd),",
    "    });",
    "    cursor = endChar;",
    "  }",
    "  return { path, query, matches, truncated: false };",
    "}",
    "",
