export const traceProbeFlow = so.program({
  name: "trace_probe_flow",
  input: z.object({
    subject: z.string(),
    body: z.string(),
  }),
  output: z.object({
    intake: z.object({
      queue: z.enum(["billing", "technical", "account", "trust-and-safety"]),
      intent: z.enum(["refund", "access", "degradation", "policy-review"]),
      customerTone: z.enum(["calm", "frustrated", "urgent"]),
    }),
    risk: z.object({
      severity: z.enum(["low", "medium", "high"]),
      escalationReason: z.enum(["none", "financial-risk", "service-risk", "policy-risk"]),
      needsHuman: z.boolean(),
    }),
    resolution: z.object({
      operatorSummary: z.string(),
      nextAction: z.string(),
      customerReply: z.string(),
    }),
    toolResults: z.object({
      fetched: z.object({
        customerId: z.string(),
        contactEmail: z.string().email(),
        orderReference: z.string(),
      }),
      eligibility: z.object({
        approved: z.boolean(),
        policy: z.string(),
      }),
      email: z.object({
        queued: z.boolean(),
        messageId: z.string(),
      }),
    }),
    traceSummary: z.object({
      expectedModelCalls: z.literal(3),
      expectedToolCalls: z.literal(3),
      observedModelCalls: z.number().int().nonnegative(),
      observedToolCalls: z.number().int().nonnegative(),
      observedComponentCount: z.number().int().nonnegative(),
      note: z.string(),
    }),
  }),
  async run(ctx, input) {
    ctx.log("Trace probe started.");

    const intake = await ctx.call(traceProbeIntake, input);
    ctx.log(
      `Stage 1 complete: queue=${intake.queue}, intent=${intake.intent}, tone=${intake.customerTone}.`,
    );

    const risk = await ctx.call(traceProbeRisk, {
      ...input,
      ...intake,
    });
    ctx.log(`Stage 2 complete: severity=${risk.severity}, needsHuman=${String(risk.needsHuman)}.`);

    const fetched = await ctx.call(fetchData, input);
    ctx.log(`Fetched customer context for ${fetched.customerId}.`);

    const eligibility = await ctx.call(checkEligibility, {
      queue: intake.queue,
      severity: risk.severity,
      needsHuman: risk.needsHuman,
    });
    ctx.log(
      `Eligibility checked: approved=${String(eligibility.approved)} via ${eligibility.policy}.`,
    );

    const resolution = await ctx.call(traceProbeResolution, {
      ...input,
      ...intake,
      ...risk,
    });
    ctx.log("Stage 3 complete: resolution draft created.");

    const email = await ctx.call(sendEmail, {
      recipient: fetched.contactEmail,
      subject: input.subject,
      preview: resolution.customerReply,
    });
    ctx.log(`Queued email notification ${email.messageId}.`);

    return {
      intake,
      risk,
      resolution,
      toolResults: {
        fetched,
        eligibility,
        email,
      },
      traceSummary: {
        expectedModelCalls: 3,
        expectedToolCalls: 3,
        observedModelCalls: ctx.trace.modelCalls.length,
        observedToolCalls: ctx.trace.toolCalls.length,
        observedComponentCount: ctx.trace.components.length,
        note: "This flow intentionally chains three predict modules and three tools so the dashboard can verify mixed trace timelines.",
      },
    };
  },
});
