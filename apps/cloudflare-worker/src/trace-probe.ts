import { z } from "zod";

import { so } from "superobjective";

export const fetchData = so.tool({
  name: "fetch_data",
  description: so.text({
    value: "Load the customer context needed for the trace probe.",
    optimize: true,
  }),
  input: z.object({
    subject: z.string(),
    body: z.string(),
  }),
  output: z.object({
    customerId: z.string(),
    contactEmail: z.string().email(),
    orderReference: z.string(),
  }),
  async execute(input) {
    const normalized = input.subject.toLowerCase().replace(/[^a-z0-9]+/g, "-");

    return {
      customerId: `cust_${normalized || "trace-probe"}`,
      contactEmail: "customer@example.com",
      orderReference: "order_trace_probe_001",
    };
  },
});

export const checkEligibility = so.tool({
  name: "check_eligibility",
  description: so.text({
    value: "Check whether the traced case should receive automated handling.",
    optimize: true,
  }),
  input: z.object({
    queue: z.enum(["billing", "technical", "account", "trust-and-safety"]),
    severity: z.enum(["low", "medium", "high"]),
    needsHuman: z.boolean(),
  }),
  output: z.object({
    approved: z.boolean(),
    policy: z.string(),
  }),
  async execute(input) {
    return {
      approved: input.severity !== "high" && !input.needsHuman,
      policy: input.queue === "billing" ? "refund-policy-v2" : "general-support-policy-v1",
    };
  },
});

export const sendEmail = so.tool({
  name: "send_email",
  description: so.text({
    value: "Queue a customer-facing email generated by the trace probe.",
    optimize: true,
  }),
  input: z.object({
    recipient: z.string().email(),
    subject: z.string(),
    preview: z.string(),
  }),
  output: z.object({
    queued: z.boolean(),
    messageId: z.string(),
  }),
  async execute(input) {
    const normalizedRecipient = input.recipient.replace(/[^a-z0-9]+/gi, "_");

    return {
      queued: true,
      messageId: `msg_${normalizedRecipient.toLowerCase()}`,
    };
  },
});

